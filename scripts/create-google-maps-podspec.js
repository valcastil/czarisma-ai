/**
 * Creates a stub react-native-google-maps.podspec inside
 * node_modules/react-native-maps/ immediately after `npm install`.
 *
 * react-native-maps >=1.27 removed the standalone podspec, but
 * Expo's withMaps plugin still references it. This script runs as
 * a postinstall hook so the file is present before `expo prebuild`
 * and `pod install`.
 */
const fs = require('fs');
const path = require('path');

const rnMapsDir = path.join(__dirname, '..', 'node_modules', 'react-native-maps');

if (!fs.existsSync(rnMapsDir)) {
  console.log('[postinstall] react-native-maps not found, skipping stub podspec.');
  process.exit(0);
}

const stubPath = path.join(rnMapsDir, 'react-native-google-maps.podspec');

const stub = [
  '# Auto-generated by scripts/create-google-maps-podspec.js',
  "require 'json'",
  "package = JSON.parse(File.read(File.join(__dir__, 'package.json')))",
  '',
  'Pod::Spec.new do |s|',
  "  s.name         = 'react-native-google-maps'",
  "  s.version      = package['version']",
  "  s.summary      = 'Shim for react-native-maps Google subspec.'",
  "  s.homepage     = package['homepage']",
  "  s.license      = package['license']",
  "  s.authors      = package['author']",
  "  s.platform     = :ios, '16.0'",
  "  s.source       = { :path => '.' }",
  "  s.dependency 'react-native-maps/Google'",
  'end',
  '',
].join('\n');

fs.writeFileSync(stubPath, stub, 'utf8');
console.log('[postinstall] ✓ wrote stub react-native-google-maps.podspec');
